#Operators
# 7 types of Arithmetic Operators:
#    1. Exponential: base and exponents -> 2**3
print(2**3)
print(2.**3.)
print(2.**3)
#    2. Multiplication (single asterisk)
print(2*3)
print(2.*3.)
print(2.*3)
#    3. Division (always returns float)
print(10/2)
print(10./2.)
print(10./2)

#    4. Floor division: in order to return integer, also it rounds the numbers
print(10//2)
print(10.//2.)
print(10.//2)

print(6/-4)
print(6//-4)

#    5. Modulo: to calculate the remainder after division
print(4%2)
print(5%2)

#    6. Addition:
print(4+2)
print(5+2)

#    7. Substraction:
print(4-2)
print(5-2)


